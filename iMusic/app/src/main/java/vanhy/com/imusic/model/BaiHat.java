package vanhy.com.imusic.model;

public class BaiHat {

    private String ten;
    private String casi;

    public BaiHat(String ten, String casi) {
        this.ten = ten;
        this.casi = casi;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public String getCasi() {
        return casi;
    }

    public void setCasi(String casi) {
        this.casi = casi;
    }
}
